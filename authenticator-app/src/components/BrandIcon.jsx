// Deterministic color from string for fallback badge
function stringToColor(str) {
  const palette = [
    '#6366F1', '#8B5CF6', '#EC4899', '#F59E0B',
    '#10B981', '#3B82F6', '#EF4444', '#14B8A6',
  ];
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  return palette[Math.abs(hash) % palette.length];
}

export default function BrandIcon({ issuer = '', size = 36 }) {
  const key = issuer.toLowerCase().trim();
  const IconComp = BRAND_ICONS[key];

  const style = {
    width: size,
    height: size,
    borderRadius: 'var(--radius-sm)',
    flexShrink: 0,
    display: 'flex',
    alignItems: 'center',
    justifyContent: 'center',
    overflow: 'hidden',
  };

  if (IconComp) {
    const bgColor = BRAND_COLORS[key] || 'var(--color-surface-strong)';
    const isDark = ['github', 'apple'].includes(key);
    return (
      <span
        style={{
          ...style,
          background: isDark ? bgColor : 'var(--color-surface-raised)',
          color: isDark ? '#fff' : bgColor,
          padding: 4,
        }}
        aria-label={issuer}
        role="img"
      >
        <IconComp />
      </span>
    );
  }

  // Fallback: colored circle with first letter
  const letter = (issuer || '?')[0].toUpperCase();
  const bg = stringToColor(issuer || '?');
  return (
    <span
      style={{
        ...style,
        background: bg,
        color: '#fff',
        fontSize: size * 0.44,
        fontWeight: 700,
        fontFamily: 'var(--font-stack)',
      }}
      aria-label={issuer}
      role="img"
    >
      {letter}
    </span>
  );
}
